import Foundation


//MARK: Enum to identify Operations that can be used in Calculator
enum Operations: String {
    case plus       = "+"
    case minus      = "-"
    case multiply   = "*"  // change that symbol
    case divide     = "/"  // change that symbol
    case plusMinus  = "+-" // change that symbol
    case percentage = "%"
    case unknown    = ""
}
